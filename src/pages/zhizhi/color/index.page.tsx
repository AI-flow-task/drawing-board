const Color = () => {
  const [colorListName, setColorListName] = useState('gugong');
  const [keyword, setKeyword] = useState('');
  const [color, setColor] = useState('#F5F2E9');
  const [colorName, setColorName] = useState('凝脂');

  useEffect(() => {
    document.title = '之之选色';
  }, [colorListName, keyword]);

  return (
    <div
      style={{
        display: 'flex',
        flexDirection: 'column',
        gap: 24,
        padding: 24,
        minWidth: '100%',
        background: color,
        transition: 'all 0.3s',
        fontFamily:
          '"Times New Roman", times, "Heti Song", serif, "Apple Color Emoji", "Segoe UI Emoji", "Segoe UI Symbol"',
      }}
      className="entry heti"
    >
      <link rel="stylesheet" href="//unpkg.com/heti/umd/heti.min.css"></link>
      {colorName ? <h1>{colorName}</h1> : null}
      <div
        style={{
          display: 'flex',
          justifyContent: 'center',
          alignItems: 'center',
        }}
      >
        <Segmented
          value={colorListName}
          onChange={(value) => {
            setColorListName(value);
          }}
          options={colorMap.map((item) => ({
            key: item.key,
            label: item.key,
            value: item.value,
          }))}
        />
      </div>
      <Input
        placeholder="搜索颜色"
        value={keyword}
        onChange={(e) => {
          setKeyword(e.target.value);
        }}
      />
      <div className={` ${styles?.['mask-card-list']}`}>
        {colorMap
          .find((item) => item.value === colorListName)
          ?.children.filter((item) => {
            if (!keyword) {
              return true;
            }

            return item.color.includes(keyword) || item.name.includes(keyword);
          })
          .map((item) => (
            <div
              key={item.color}
              className={styles?.['mask-cards']}
              style={{
                borderRadius: 12,
                padding: 12,
                boxShadow: '0 4px 6px -1px rgb(0 0 0 / 0.1), 0 2px 4px -2px rgb(0 0 0 / 0.1)',
                width: '100%',
                transition: 'all 0.3s',
                //@ts-ignore
                '--bg': item.color,
                backgroundColor: 'var(--bg)',
                marginBottom: 10,
              }}
              onClick={() => {
                setColor(item.color);
                setColorName(item.name);
              }}
            >
              <div style={{ color: 'color-contrast(var(--bg) vs #fff, #000)' }}>{item.name}</div>
              <div
                style={{
                  color: 'color-contrast(var(--bg) vs #fff, #000)',
                }}
              >
                {item.color?.toUpperCase()}
              </div>
              <div
                style={{
                  color: 'color-contrast(var(--bg) vs #fff, #000)',
                }}
              >
                {hexToRgb(item.color)}
              </div>
            </div>
          ))}
      </div>
    </div>
  );
};

export default Color;
